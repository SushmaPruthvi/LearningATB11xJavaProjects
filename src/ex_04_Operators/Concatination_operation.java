package ex_04_Operators;

public class Concatination_operation {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a+b); // now the "+" will work as the add operator

        String name = "Sushma";
        String newname = "S";
        System.out.println(name+newname); // here it will behave as the concatination operator
    }
}
