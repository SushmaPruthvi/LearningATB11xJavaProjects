package ex_04_Operators;

public class Compound_Assignment_Operator {
    public static void main(String[] args) {

        // += , -=, *=, /=

        int age = 50;
        age += 10;
        System.out.println(age);

        int a = 30;
        a -= 10;
        System.out.println(a);

        int b = 30;
        b /= 10;
        System.out.println(b);

    }
}
