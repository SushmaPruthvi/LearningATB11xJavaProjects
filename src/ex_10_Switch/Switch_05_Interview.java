package ex_10_Switch;

public class Switch_05_Interview {
    public static void main(String[] args) {
        char n1 = 'B';

        switch (n1)
        {
            case 65:
                System.out.println("ASCII value is 65");
                break;

            case 66:
                System.out.println("ASCII value is 66");
                break;
        }
    }
}
