package ex_10_Switch;

public class Switch_13_Interview {
    public static void main(String[] args) {
        int a = 10;

        switch (-1)
        {
            case -1:
                System.out.println("the value -1 is printing");
                break;

            case 10:
                System.out.println("the value 10 is printing");
                break;

            default:
                System.out.println("default value");
                break;
        }
    }
}
