package ex_09_For_Loop;

public class For_Loop_IfElse_06 {
    public static void main(String[] args) {
        for(int i=0;i<10;i++)
        {
            if(i>5)
            {
                System.out.println("The values below the 5 number is printed");
            }
            else
            {
                System.out.println("The values above the 5 number is printed");
            }
        }
    }
}
