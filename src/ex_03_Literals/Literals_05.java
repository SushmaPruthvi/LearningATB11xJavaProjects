package ex_03_Literals;

public class Literals_05 {
    public static void main(String[] args) {

        //escape character
        System.out.println("SushmaPruthviraj");
        System.out.println("Sushma \n Pruthviraj");
        System.out.println("Sushma \t Pruthviraj");
        System.out.println("Sushma\bPruthviraj");
        System.out.println("Sushma\rPruthviraj");
    }
}
